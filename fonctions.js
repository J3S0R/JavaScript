// Une fonction en JavaScript est un bloc de code réutilisable que l'on peut appeler en utilisant son nom
// C'est un type d'objet
// Une fonction peut être appelée n'importe ou dans le code, même si on l'appele plusieurs lignes avant et qu'elle est définie plusieurs lignes après, ce qui est différent d'une variable

// Exemple : on crée la fonction 
function saluer (nom) {
    console.log(`Bonjour ! ${nom} comment ça va ?`)
}

// Appeler la fonction
saluer("Alice");
saluer("Bob");




